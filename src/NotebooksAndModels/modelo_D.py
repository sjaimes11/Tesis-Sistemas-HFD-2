from sklearn.inspection import permutation_importance
from sklearn.base import BaseEstimator, ClassifierMixin

class KerasWrapper(BaseEstimator, ClassifierMixin):
    def __init__(self, keras_model):
        self.keras_model = keras_model
        self.classes_ = np.array([0, 1])
    def fit(self, X, y):
        return self
    def predict(self, X):
        return (self.keras_model.predict(X, verbose=0).flatten() >= 0.5).astype(int)

wrapper = KerasWrapper(model)
perm = permutation_importance(wrapper, X_test_sc, y_test, n_repeats=5,
                              random_state=SEED, scoring="f1", n_jobs=1)

indices = np.argsort(perm.importances_mean)[::-1]
