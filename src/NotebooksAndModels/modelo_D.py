# CELDA 12: Verificar precision del TFLite int8
# ============================================================
interpreter = tf.lite.Interpreter(model_path=str(tflite_int8_path))
interpreter.allocate_tensors()

input_details  = interpreter.get_input_details()
output_details = interpreter.get_output_details()

input_scale, input_zero  = input_details[0]["quantization"]
output_scale, output_zero = output_details[0]["quantization"]

print(f"Input  : dtype={input_details[0]['dtype']}, scale={input_scale}, zero={input_zero}")
print(f"Output : dtype={output_details[0]['dtype']}, scale={output_scale}, zero={output_zero}")

y_pred_tflite = []
for i in range(len(X_test_sc)):
    sample = X_test_sc[i:i+1].astype(np.float32)
    sample_q = (sample / input_scale + input_zero).astype(np.int8)

    interpreter.set_tensor(input_details[0]["index"], sample_q)
    interpreter.invoke()

    output_q = interpreter.get_tensor(output_details[0]["index"])
    output_f = (output_q.astype(np.float32) - output_zero) * output_scale
    pred = 1 if output_f.flatten()[0] >= 0.5 else 0
    y_pred_tflite.append(pred)

y_pred_tflite = np.array(y_pred_tflite)

acc_tflite = accuracy_score(y_test, y_pred_tflite)
f1_tflite  = f1_score(y_test, y_pred_tflite)

