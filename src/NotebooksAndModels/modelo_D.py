for i, idx in enumerate(indices):
    bar = "█" * int(perm.importances_mean[idx] * 100)
    print(f"  {i+1:2d}. {FLOW_FEATURES[idx]:<20s} {perm.importances_mean[idx]:.4f}  {bar}")

